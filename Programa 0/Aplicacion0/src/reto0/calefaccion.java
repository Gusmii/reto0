package reto0;

public class calefaccion {

	private String fecha;
	private int piso;
	
	
	
	public calefaccion(String fecha, int piso) {
		
		this.fecha = fecha;
		this.piso = piso;
	}
	
	public String getFecha() {
		return fecha;
	}
	public void setFecha(String fecha) {
		this.fecha = fecha;
	}
	public int getPiso() {
		return piso;
	}
	public void setPiso(int piso) {
		this.piso = piso;
	}
	
	
	
}
